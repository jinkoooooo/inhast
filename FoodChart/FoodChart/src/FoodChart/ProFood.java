    public void getDBData(String strQuery){
        String strOutput = "날짜\t시간\t코너\t가격\t메뉴\n";
        jTextArea1.setText(strOutput);
        try{
            DBM.DB_rs = DBM.DB_stmt.executeQuery(strQuery);
            while(DBM.DB_rs.next()){
                strOutput = "";
                strOutput += DBM.DB_rs.getString("Day")+"\t";
                strOutput += DBM.DB_rs.getString("Type")+"\t";
                strOutput += DBM.DB_rs.getString("Corner")+"\t";
                strOutput += DBM.DB_rs.getString("Price")+"\t";
                strOutput += DBM.DB_rs.getString("Content")+"\n";
                
                jTextArea1.append(strOutput);
            }
            DBM.DB_rs.close();
        }catch (Exception e){
            System.out.println("SQLException: "+e.getMessage());
        }
    }
    
    public String makeSQLWhere(String strQuery){
        switch(cboSearch.getSelectedIndex()){
            case 0:
            strQuery += " Where foodchartp.Day = '" + txtDate.getText() + "'";
            break;
            case 1:
            strQuery += " Where Type = '" + txtType.getText() + "'";
            break;
            case 2:
            strQuery += " Where Corner = '" + txtCorner.getText() + "'";
            break;
            case 3:
            strQuery += " Where Content = '" + txtContent.getText() + "'";
            break;
            case 4:
            strQuery += " Where Price = '" + txtPrice.getText() + "'";
            break;
            default:
                strQuery = "Select * From foodchartp";
                break;
            
        }
        return strQuery;
    }

